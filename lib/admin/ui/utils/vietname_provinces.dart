  static List<String> searchProvinces(String searchText) {
    List<String> result = [];
    String alteredSearchText = Utils.removeVietnameseAccent(searchText);
    for (String province in provinces) {
      String alteredProvince = Utils.removeVietnameseAccent(province);
      if (alteredProvince
          .toLowerCase()
          .contains(alteredSearchText.toLowerCase())) {
        result.add(province);
      }
    }
    return result;
  }

  static ProvinceLocation getProvinceLocation(String provinceName) {
    String formattedProvinceName =
        Utils.removeVietnameseAccent(provinceName).toLowerCase();
    return provinceLocations.firstWhere((province) {
      String formattedProvince =
          Utils.removeVietnameseAccent(province.name).toLowerCase();
      return formattedProvince == formattedProvinceName;
    });
  }
